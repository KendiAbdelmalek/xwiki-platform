package org.xwiki.model;

/**
 * Any checked exception generated by the Model.
 *
 * @version $Id$
 * @since 4.3M1
 */
public class ModelException extends Exception
{
    /**
     * Class ID for serialization.
     */
    private static final long serialVersionUID = 1L;

    /**
     * Constructs a new exception with the specified detail message. The cause is not initialized, and may
     * subsequently be initialized by a call to {@link #initCause(Throwable)}.
     *
     * @param message the detail message (which is saved for later retrieval by the {@link #getMessage()} method)
     */
    public ModelException(String message)
    {
        super(message);
    }

    /**
     * Constructs a new exception with the specified detail message and cause.
     *
     * @param message the detail message (which is saved for later retrieval by the {@link #getMessage()} method)
     * @param parameters the parameter to use to resolve the message
     *        (using {@link String#format(String, java.lang.Object...)})
     */
    public ModelException(String message, java.lang.Object... parameters)
    {
        super(String.format(message, parameters));
    }

    /**
     * Constructs a new exception with the specified detail message and cause.
     *
     * @param message the detail message (which is saved for later retrieval by the {@link #getMessage()} method)
     * @param parameters the parameter to use to resolve the message
     *        (using {@link String#format(String, java.lang.Object...)})
     * @param cause the cause (which is saved for later retrieval by the {@link #getCause()} method). A null value is
     *        permitted, and indicates that the cause is nonexistent or unknown
     */
    public ModelException(String message, Throwable cause, java.lang.Object... parameters)
    {
        super(String.format(message, parameters), cause);
    }
}